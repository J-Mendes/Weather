import UIKit

extension UIColor {

    // MARK: - Colors
    
    class func neutralBlueColor() -> UIColor {
        return UIColor.init(colorLiteralRed: 100.0 / 255.0, green: 150.0 / 255.0, blue: 208.0 / 255.0, alpha: 1.0)
    }
    
    class func lightBlueColor() -> UIColor {
        return UIColor.init(colorLiteralRed: 43.0 / 255.0, green: 136.0 / 255.0, blue: 216.0 / 255.0, alpha: 1.0)
    }
    
}
